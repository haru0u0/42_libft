int ft_atoi(const char *nptr)
{
	int result;

	result = 0;
	while (*nptr != '\0' && (48 <= *nptr && *nptr <= 57))
	{
		result = result * 10 + (*nptr - '0');
		nptr++;
	}
	return (result);
}

